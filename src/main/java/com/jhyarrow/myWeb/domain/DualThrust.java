package com.jhyarrow.myWeb.domain;

public class DualThrust {
	private String stockCode;
	private String buyDate;
	private float buyAmt;
	private String sellDate;
	private float sellAmt;
	private String benifit;
	public String getStockCode() {
		return stockCode;
	}
	public void setStockCode(String stockCode) {
		this.stockCode = stockCode;
	}
	public String getBuyDate() {
		return buyDate;
	}
	public void setBuyDate(String buyDate) {
		this.buyDate = buyDate;
	}
	public float getBuyAmt() {
		return buyAmt;
	}
	public void setBuyAmt(float buyAmt) {
		this.buyAmt = buyAmt;
	}
	public String getSellDate() {
		return sellDate;
	}
	public void setSellDate(String sellDate) {
		this.sellDate = sellDate;
	}
	public float getSellAmt() {
		return sellAmt;
	}
	public void setSellAmt(float sellAmt) {
		this.sellAmt = sellAmt;
	}
	public String getBenifit() {
		return benifit;
	}
	public void setBenifit(String benifit) {
		this.benifit = benifit;
	}
	
	
	
}
